// доведены до outcome (см. ugc). Поля stages/kind/detail — структурный слой.

export type AARRRStage =
  | 'acquisition'
  | 'activation'
  | 'conversion'
  | 'retention'
  | 'referral'
  | 'revenue'

export type ModuleKind = 'funnel' | 'enablement' | 'core'

export interface GrowthModule {
  id: string
  index: string
  title: string
  stageLabel: string
  /** Какие стадии воронки закрывает (пусто у enablement/core — они рама). */
  stages: AARRRStage[]
  kind: ModuleKind
  does: string
  metric: string
  bet: string
  status: string
  link?: string
  /** Раскрытие карточки — наш слой, голос «мы», где нет данных — гипотеза. */
  detail: string
}

