export function moduleById(id: string): GrowthModule | undefined {
  return MODULES.find((m) => m.id === id)
}

/** Стадии AARRR для сигнатурной воронки — каждая со своим срезом (стадия не дублирует модуль). */
export interface FunnelStage {
  key: AARRRStage
  index: string
  label: string
  /** Модуль, закрывающий стадию. */
  moduleId: string
  /** Что именно делает эта стадия (стадио-специфично). */
  focus: string
  /** Метрика стадии. */
  metric: string
  /** Наша ставка на этой стадии. */
  bet: string
}

